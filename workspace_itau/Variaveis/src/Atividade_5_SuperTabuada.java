
public class Atividade_5_SuperTabuada {

	public static void main(String[] args) {
		
				
		for (int contador = 0; contador < 10; contador++) {
			System.out.println("-----------");
			System.out.println("Tabuada do "+(contador+1));
			System.out.println("-----------");
			for (int contador1 = 0; contador1 < 10; contador1++) {
				
				System.out.println((contador+1)+" x "+(contador1+1)+" = "+(contador+1)*(contador1+1));
			}
			
			}
				
		}


	}


