
public class Laco {

	public static void main(String[] args) {
		System.out.println("WHILE");
		int contador=0;
		do {
			System.out.println(contador);
			contador++;
		}
		while(contador<5);
		
		
		
		//criando mesmo exemplo com FOR;
		System.out.println("FOR");
		for(int cont=0; cont<5;cont++) {
			System.out.println(cont);
		}
		
	}
	
}
